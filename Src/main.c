/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{
    uint16_t key_press_time = 0;
        
    /* USER CODE BEGIN 5 */
    DBG_SendMessage(DBG_MSG_TASK_STATE, APP_VER_DBG);
    //DBG_SendMessage(DBG_MSG_TASK_STATE, "Default Task Start\r\n");
    
    /* Create WiFi Control Task */
    xTaskCreate( WiFi_ControlTask,
                "WiFi", 
                CFG_STACK_WIFI,
                (void *) 0,
                CFG_PRIORITY_WIFI,
                NULL);
    
    /* Create Client Communication Task */
    xTaskCreate( Client_CommTask,
                "Client", 
                CFG_STACK_CLIENT,
                (void *) 0,
                CFG_PRIORITY_CLIENT,
                NULL);
    
    /* Create Motor Control Task */
    xTaskCreate( Motor_ControlTask,
                "Motor", 
                CFG_STACK_MOTOR,
                (void *) 0,
                CFG_PRIORITY_MOTOR,
                NULL);
    
    /* Create Display Task */
    xTaskCreate( Disp_ShowTask,
                "Display", 
                CFG_STACK_DISPLAY,
                (void *) 0,
                CFG_PRIORITY_DISPLAY,
                NULL);
    
    /* Create Camera Photo Task */
    xTaskCreate( Camera_PhotoTask,
                "Camera", 
                CFG_STACK_CAMERA,
                (void *) 0,
                CFG_PRIORITY_CAMERA,
                NULL);
    
    /* Create Camera Save Task */
    xTaskCreate( Camera_SaveTask,
                "Save", 
                CFG_STACK_SAVE,
                (void *) 0,
                CFG_PRIORITY_SAVE,
                NULL);  
    
#ifdef EN_DEBUG
    /* Create Debug message print task for program state and information */
    xTaskCreate(DBG_PrintTask, 
                "Debug", 
                CFG_STACK_DEBUG,
                (void *) 0, 
                CFG_PRIORITY_DEBUG, 
                NULL); 
#endif
    
    /* Infinite loop */
    for(;;)
    {
        if(HAL_GPIO_ReadPin(KEY1_GPIO_Port, KEY1_Pin) == GPIO_PIN_RESET)
        {
            key_press_time++;
            
            /* long press 3second to erase config */
            if(key_press_time > 30)
            {
                Mem_ResetConfig();
                vTaskDelay(1000 / portTICK_PERIOD_MS);
                HAL_NVIC_SystemReset();
            }
        }
        else
        {
            key_press_time = 0;
        }
        
        osDelay(100);
    }
    /* USER CODE END 5 */ 
}

